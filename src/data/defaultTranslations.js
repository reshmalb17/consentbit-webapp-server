/**
 * Merge stored translations with defaults (per-language so missing keys fall back to default).
 */
export function mergeTranslations(stored) {
  if (!stored || typeof stored !== 'object') return DEFAULT_TRANSLATIONS;
  const result = { ...DEFAULT_TRANSLATIONS };
  for (const lang of Object.keys(stored)) {
    result[lang] = { ...(DEFAULT_TRANSLATIONS[lang] || {}), ...stored[lang] };
  }
  return result;
}
